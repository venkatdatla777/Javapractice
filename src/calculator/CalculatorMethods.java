package calculator;

class CalculatorMethods {

        public int Add(int num1, int num2)     //created add,sub,mul,div methods here
        {
            return (num1 + num2);
        }
        public int Sub(int num1, int num2)
        {
            return (num1 - num2);
        }
        public int Mul(int num1, int num2)
        {
            return (num1 * num2);
        }
        public int Div(int num1, int num2)
        {
            return (num1 / num2);
        }
}

